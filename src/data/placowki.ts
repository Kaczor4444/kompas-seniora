export function getPlacowkaById(id: string): Placowka | undefined {
  return allPlacowki.find(p => p.id === id);
}

export function getPlacowkiByTyp(typ: 'DPS' | 'ŚDS'): Placowka[] {
  return allPlacowki.filter(p => p.typ_placowki === typ);
}

export function getPlacowkiByMiasto(miasto: string): Placowka[] {
  const normalized = miasto.toLowerCase().trim();
  return allPlacowki.filter(p => 
    p.miasto_wies.toLowerCase().includes(normalized) ||
    p.gmina.toLowerCase().includes(normalized) ||
    p.powiat.toLowerCase().includes(normalized)
  );
}